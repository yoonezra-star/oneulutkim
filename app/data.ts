export function getCategory(categoryId: CategoryId | string) {
  return categories.find((category) => category.id === categoryId);
}

export function getPostsByCategory(categoryId: CategoryId | string) {
  return posts.filter((post) => post.category === categoryId);
}

export function getPost(slug: string) {
  return posts.find((post) => post.slug === slug);
}

export function getCategoryName(categoryId: CategoryId) {
  return getCategory(categoryId)?.name ?? "게시판";
}

export function isCategoryId(categoryId: string): categoryId is CategoryId {
  return categories.some((category) => category.id === categoryId);
}

export function isSortMode(sortMode: string): sortMode is SortMode {
  return sortOptions.some((option) => option.id === sortMode);
}

export function getPostTextLength(post: Post) {
  return [post.title, post.summary, ...post.body].join("").length;
}

export function getReadingMinutes(post: Post) {
  return Math.max(1, Math.ceil(getPostTextLength(post) / 500));
}

export function getEditorialScore(post: Post) {
  return Math.min(
    99,
    40 +
      post.tags.length * 4 +
      post.body.length * 5 +
      (post.label ? 8 : 0) +
      Math.floor(getPostTextLength(post) / 120),
  );
}

export function getTopicScore(post: Post) {
  return post.tags.length * 10 + post.body.length * 4 + (post.label ? 8 : 0);
}

export function sortPosts(postList: Post[], sortMode: SortMode = "latest") {
  return [...postList].sort((a, b) => {
    if (sortMode === "recommends") {
      return getEditorialScore(b) - getEditorialScore(a);
    }

    if (sortMode === "views") {
      return getPostTextLength(b) - getPostTextLength(a);
    }

    if (sortMode === "comments") {
      return getTopicScore(b) - getTopicScore(a);
    }

    return b.id - a.id;
  });
}

export function filterPosts({
  query = "",
  category,
  sort = "latest",
}: {
  query?: string;
  category?: string;
  sort?: string;
}) {
  const normalized = query.trim().toLowerCase();
  const validCategory = category && isCategoryId(category) ? category : undefined;
  const validSort = sort && isSortMode(sort) ? sort : "latest";

  const filtered = posts.filter((post) => {
    if (validCategory && post.category !== validCategory) {
      return false;
    }

    if (!normalized) {
      return true;
    }

    const haystack = [
      post.title,
      post.summary,
      post.author,
      getCategoryName(post.category),
      ...post.tags,
    ]
      .join(" ")
      .toLowerCase();

    return haystack.includes(normalized);
  });

  return sortPosts(filtered, validSort);
}

export function searchPosts(query: string) {
  const normalized = query.trim();

  if (!normalized) {
    return [];
  }

  return filterPosts({ query: normalized });
}

export function getIssueKeywords(limit = 30, sourcePosts: Post[] = posts) {
  const tagStats = new Map<string, { tag: string; count: number; score: number }>();

  for (const post of sourcePosts) {
    for (const tag of post.tags) {
      const current = tagStats.get(tag) ?? { tag, count: 0, score: 0 };
      current.count += 1;
      current.score += getEditorialScore(post) + Math.floor(getPostTextLength(post) / 100);
      tagStats.set(tag, current);
    }
  }

  return [...tagStats.values()]
    .sort((a, b) => b.score - a.score || b.count - a.count || a.tag.localeCompare(b.tag, "ko"))
    .slice(0, limit);
}

